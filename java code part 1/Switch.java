import  java.util.Scanner;
class Switch {
    public  static  void main(String args[]) {
        int a;
        Scanner obj = new Scanner(System.in);
        a = obj.nextInt();
        switch (a){
            case 1:
                System.out.println(1);
                break;
            case 2 :
                System.out.println(2);
                break;


        }
    }
}
